using System;

namespace Calculadora
{
    public class Operaciones
    {
        public static int  Suma(int n1, int n2) => n1 + n2;
        public static int  Resta(int n1, int n2) => n1 - n2;
        public static int  Multiplicacion(int n1, int n2) => n1 * n2;
        public static int Division(int n1, int n2)  => n1 / n2;
    }
}


